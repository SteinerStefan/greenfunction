#define fileNameLength 100
//-------------------------------------------------------------------------------------------------------------------------------
//Includes
//-------------------------------------------------------------------------------------------------------------------------------
#include <stdio.h>
#include "stdlib.h"
#include "timeinfo.h"
//-------------------------------------------------------------------------------------------------------------------------------
//getMinuten
//-------------------------------------------------------------------------------------------------------------------------------
int getMinutes(int seconds)
{
	seconds -= (seconds/3600)*3600;
	return seconds/60;
}
//-------------------------------------------------------------------------------------------------------------------------------
//getStunden
//-------------------------------------------------------------------------------------------------------------------------------
int getHours(int seconds)
{
	return seconds/3600;
}
//-------------------------------------------------------------------------------------------------------------------------------
//getSeconds
//-------------------------------------------------------------------------------------------------------------------------------
int getSeconds(int seconds)
{
	seconds -=(seconds/3600)*3600;
	return seconds -= (seconds/60)*60;
}
//-------------------------------------------------------------------------------------------------------------------------------
//writeTime2File
//-------------------------------------------------------------------------------------------------------------------------------
//int writeTime2File(int timerArrC, int *timerArrV, char *dataFolderName)
int writeTime2File(time_t startTime,char* info, char *dataFolderName)
{
	time_t currentTime;
	time(&currentTime);
	double seconds = currentTime-startTime;

	char path[fileNameLength];
	FILE *timedatei;	
	snprintf(path, fileNameLength, "%s/timing.txt", dataFolderName);	
	timedatei = fopen(path, "a"); //writing	
    if (timedatei == NULL)
    {
		perror("Fehler beim oeffnen der Datei");
        return EXIT_FAILURE;
    }
 	fprintf(timedatei, "%s: %02d:%02d:%02d\n",info,getHours(seconds),getMinutes(seconds), getSeconds(seconds));
	fclose(timedatei);
	return 0;
}
//-------------------------------------------------------------------------------------------------------------------------------
//plotTime
//-------------------------------------------------------------------------------------------------------------------------------
int plotTime(int *timerArrV, int wert)
{
	printf("Programmlaufzeit: %02d:%02d:%02d\n",getHours(timerArrV[wert]),getMinutes(timerArrV[wert]), getSeconds(timerArrV[wert]) );
	return 0;
}
